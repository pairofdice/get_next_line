#include "get_next_line.h"

int	get_next_line(const int fd, char **line)
{
	int		ret;
	char	buff[BUFF_SIZE + 1];

	ret = read(fd, buff, BUFF_SIZE);
	write(1, buff, ret);
	return (ret);
}
